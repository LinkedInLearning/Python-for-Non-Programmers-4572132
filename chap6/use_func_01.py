print("Hello")

#変数を引数として受け取り、出力することもできます。
a = 10
print(a)

#また、複数の引数を受け取り、出力することもできます。
a = 1
b = 2
print(a,b)

#引数は二つとは限りません。print関数の場合、いくつでも引数を受けるとことができます。
a = 1
b = 2
c = 3
d = 4
e = 5
print(a,b,c,d,e)

#ほかの関数も見ていきましょう

#関数は引数を受け取るだけでなく、値を返すこともできます。
#abs関数は数値を受け取り、絶対値を返す関数です。
x = abs(-100)
print(x)


